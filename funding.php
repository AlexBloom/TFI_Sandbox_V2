			<h2>How To Raise Funds?</h2>
			<p>By Laura Bandel,Director of Institutional Development, at Tribeca Film Institute</p>

			<p class="intro">As a Director of Institutional Development here at TFI, I know first hand how difficult fundraising for your project can prove to be. It’s an incredibly competitive space to get your voice heard and project funded, but a few simple strategies can help you to stand out from the pack. Here is my golden list of do's and don'ts.</p>


			<h3>DO</h3>
			
			<ul>	
				<li>	<p><strong>Read the instructions. </strong> ALL OF THEM. Probably twice.  It is very easy to skip taking a deep dive into the specifics of an RFP and get right to typing into the text box on the application page, but this can set you back hours in the process. Institutional funders often use the same form for different applications, so you may encounter a directive like:  “Leave sections A through C blank. Fill in Section D with one sentence that describes your project.” If your proposal comes along with part of the application filled in that didn't need to be, it most likely goes into the trash. 
				</p></li>
				<li>	<p><strong>Tell your story to a novice audience. </strong> Never assume the funder you are pitching your project to understand tech jargon (or any type of jargon for that matter). Even funders who specialize in supporting projects similar to yours may be inviting people to serve on the review panel who don’t have any prior knowledge of the field you are working in. A jargon-free proposal also ensures you are being crystal clear about what it is you hope to achieve and how. Remember: leaning on verbal shorts = passive and vague descriptions. 
				</p></li>
				<li>	<p><strong>Be specific. </strong> A trap I often catch myself in is getting so consumed in explaining the moving parts of a project, I forget to give enough color or context to my writing. Get as specific as possible, and don’t be afraid to name drop (as long as you have firmly committed partnerships with said names you’re dropping). For example: “TFI has several partners committed to supporting the TFI New Media Fund” versus “The TFI New Media Fund is funded by several leading organizations in the art and social justice fields, including the Ford Foundation and the National Endowment for the Arts.” 
				</p></li>
				<li>	<p><strong>Include evaluation plans. </strong>Plans for documenting impact is a thing of beauty to the people you are asking to support your project. In many cases, the program officers and Corporate Social Responsibility employees at the institutions you are appealing to will have to present the results of their philanthropy portfolio to their bosses at the end of each year (or more often than that). A clearly laid out evaluation plan means they will not have to struggle to tell the story of your success to higher-ups. Few of us are trained as professional evaluators or statisticians but something as simple as an online survey can go a long way to collecting quantitative data about your project’s impact. 
				</p></li>
				<li>	<p><strong>Place your project in the right frame. </strong> Institutional funders all operate to achieve specific missions in the causes they work to advance. With that in mind, play up the parts of your project that speak to each funder’s mission in the short amount of space you have. Make connections to why your project will help that funder achieve their goals. For example, if you are writing a proposal to a social justice-based foundation, don’t spend three pages talking about how the back-end of your website works.  Sharing the story of how one person/group of people have been impacted by your project would go further in catching their attention. 
				</p></li>
				<li>	<p><strong>Proofread! </strong> When you’ve spent hours upon hours reviewing project proposals, poor grammar and careless spelling can feel like a personal affront to your sanity. Your proposal deserves the same polish as your project. If an applicant hasn't taken the time to distinguish the difference between your and you're, funders may become suspucuous of their abiity to carry out a complex project. 
				</p></li>
			</ul>

			<h3>DON’T</h3>
			<ul>
				<li> <p><strong>Fail to make the case. </strong> To borrow some wisdom from the world of design: the best solution is the best definition of a problem. Non-profit foundations and corporate CSR programs are change-makers, so what are you looking to change?  There are many wonderful ideas for charitable projects, but a plan that fills an immediate, unanswered need lends a sense of urgency and social relevance to your proposal. Some questions to ask yourself: what problem am I responding to?; why is my project the best solution to this problem?; and what made me passionate to start this project in the first place?
				</p></li>
				<li> <p><strong>Be afraid to use a footnote. </strong> Using parenthetical documentation to support of the validity of the issue you are responding to can be a very powerful way to make your case. 
				</p></li>
				<li> <p><strong>Forget about your team. </strong> The qualifications and expertise of your project team can be incredibly compelling and should not be looked over when crafting a proposal. A proven track record of success with previous projects makes funders trust you are going to be able to complete and succeed in what you propose. If this is your first time working on a web-native project, your greatest accomplishments in other areas and demonstrated problem-solving skills can be equally persuasive.   
				</p></li>
				<li> <p><strong>Feel like longer is better. </strong> Your succinct and to-the-point narrative will only make the people reviewing 100 other proposals love you more. It can be easy to repeat yourself throughout different section of the proposal without realizing it. Having someone unfamiliar with your project read over your narrative can be super helpful for avoiding such pitfalls. 
				</p></li>
				<li> <p><strong>Use staples. </strong> Within every proposal package, think about creating ease of use for the person receiving it. If a proposal is delivered by hard copy, the narrative will likely need to be photocopied and staples are terribly annoying to pull out (paperclips all the way). Name your electronic files in a convention that will make it easier for the funder to file (ie “nameofyourproject.pdf” versus “nameofthefoundationtheyworkfor.pdf). It may seem superfluous, but small touches like this go a long way to building trust in your capabilities in the small window of time the funder spends with your proposal. 
				</p></li>
				<li> <p><strong>Be annoying. </strong> There is no good that will come of being combative when faced with a declination letter. It’s a small world, but the world of philanthropy is even smaller and you risk damaging your reputation at the organization declining your proposal and any number of others in their network. Use a ‘no’ to start a relationship with that group – sign up for their newsletter, attend any public programming they might offer, and certainly try again next year.  So many excellent projects funded by TFI have come on their second or third proposals. We’ve often referred outstanding proposals to other funding organizations that that project better aligns with, so always put your best foot forward. 
				</p></li>
			</ul>
			
