						<h2>New Media &amp; Education</h2>

						<small>By Caitlin Meissner, Education Programs Manager at Tribeca Film Institute</small>

						<h4>TYWLS Bronx</h4>

						<p>The sixth grade girls at The Leadership School of the Bronx used the first semester of 2014 to create a film about their school, and experimented with online new media platforms such as Mozilla Popcorn, Pixton Comics, Aniboom and Shapeshifter.</p>
				
						<p>The girls are now putting their skills to use in cultural exchange.  In collaboration with Y-FI Africa and Precious Blood Girls Secondary School in Nairobi, Kenya, students are co-writing scripts via google docs, critiquing each other's work, designing storyboard in Pixton, filming different scenes, annotating those scenes in Mozilla Popcorn, and then combining them to make a single film. Thus far, it seems like all the young women are interested in creating a film about bullying in different cultures, or creating a film about two family members who grow up in different societies. We’ll see which storyline they choose!</p>
				
						<h4>TYWLS Astoria, East River Academy, Rikers Island and The Lamp</h4>
				
						The young women at The Leadership School of Astoria are engaged in a collaborative film exchange with young women at East River Academy on Rikers Island, where cameras are not allowed on premises. Beginning with rewriting the narrative to Nas’ classic song, “If I Ruled the World,” the women’s powerful poems of imagination and agency were voice recorded and send in to TYWLS Astoria, a high school in the same zip code as Rikers Island. TYWLS students contributed to the exchange by filming visuals to the poems, both interpreting their words and capturing images directed by the women at East River Academy. In final exchange, both classrooms will edit the footage to be screened at the Tribeca Film Festival.
				
						<p>Adding a new media component, New York based organization, The Lamp, will be entering the collaboration in a remix capacity. Bringing their web-based Lamplatoon tool into the process, TYWLS Astoria students will be tasked with remixing their own created content, adding in web-media (news clips, etc.) and voice recordings from pen pal exchanges with their collaborators at East River Academy.</p>
				
						<h4>Brooklyn International High School</h4>
				
						<p>In partnership with the Department of Education’s DigiReady initiative, students at Brooklyn International High School are prepping to create their first film and web-based interactive exploration of identity. Currently in the brainstorming stages, Brooklyn International students first learned filmmaking through the creation of a narrative short that expresses their love of the arts, multiple identities and diverse cultural community. The high school has students from over thirty countries, and all are immigrant newcomers, adding robust, textured perspectives on what it means to be a high school student in New York City.</p>
