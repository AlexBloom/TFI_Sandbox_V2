					<strong>How do you envision to bring story, design and technology together?</strong> 

					<p>The OAKLAND FENCE PROJECT is a collaborative, large-scale photo fence exhibit, live and online, using documentary photography, video, augmented reality, open data, and vibrant community partnerships to address the problems of a city in decline and help ignite collaborative urban renewal. We want people coming together just as story, design, and technology come together.</p>

					<p>Five- foot tall documentary photographs by Oakland artists are the soul of this project. They tell the story of the city, they map the soul of the urban imagination, the struggles, the challenges, the incredible beauty, the hope of the future. The integrated use of photography, audio/video, data and augmented reality enables us to produce a creative intervention that is desperately needed in this city, where the voices of the people can be heard and civic action can be catalyzed. </p>

					<p>Photo fences around the city disrupt the urban landscape in surprising ways. People stop and look at the big photographs. They talk, they wonder. They hold their phone to the images - and through the technology - are captivated by the subjects coming to life through video, audio, text on screen and data about the issue being explored. They share it with their friends. They can buy the photograph directly from the artist. They can make a donation to the local nonprofit organization working on the issue, or they can volunteer their time. They can get more information or calendar an upcoming event. The data about crisis morphs into data about change. The numbers move as we are moved to participate. The Oakland Fence project is partnered with Four Kitchens Design, Metaio Technologies and Open Oakland, a group of technologists, journalists and city staff collaborating to improve life in the city using open data & web technologies. This collaboration enables us to work with a team of visual artists to tell the stories, design a web/mobile interface and live experience that reflects the power of those stories and connects them to community action in tangible ways. We can map the needs and the neighborhoods, and through the data  - track and share our impact and the changing face of Oakland - as the project unfolds. The project is the stories; the web and production design uplifts those stories and creates an open framework for participation; the technology facilitates hand-to-hand engagement and allows empathy to become right action.</p>


					<strong>What are you looking to achieve by building out your story this way? </strong>

					<p>I love the power of large-scale photography to change how we see our day-to-day world, to magnify what we refuse to see, to provide hope, promise, and create a sense of belonging among strangers.  That’s really why. I also want to prove the concept that art does catalyze change, that artists should be at every table where problem solving is happening, and that a creative economy is essential to life. I hope the Oakland Fence Project is a collective experience that actually gains in value and meaning as it is shared. The opposite of the Facebook like or the retweet – where stuff lessens in value as it goes viral. “Here, you should read this because I don’t have time, and by the time you see it, it’s probably over anyway.” Social storytelling must be about people. We need more people coming together live and online, taking up cameras instead of guns, finding our way out of violence, poverty and hunger through the sheer force of our collective commitment to it, fortified by the creative imagination of an engaged public.</p> 

