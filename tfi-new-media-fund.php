					<strong>How do you envision to bring story, design and technology together?</strong>

					<p>When we began developing the Quipu Project we realized one of the distinct narrative challenges our project faced was finding a way to go beyond telling a story “about” those affected by the Peruvian sterilization policy, but to tell a story “with” them. At the heart of the problem lied the issues of abuse and neglect and we were committed to think hard on how not to replicate to the best of our abilities the methods through which media either ignores or exploits the stories of hard to reach communities without any explicit benefit for the subjects of the story.</p>
					<p>The people affected by the Peruvian sterilization policy are mostly illiterate, living in rural or urban marginal areas geographically dispersed all across the country, and whose access to media is almost exclusively through radio and phones. So our starting point was to first of all acknowledge these specific set of constraints, so that the story could be created and experienced by the contributors themselves. This set of cultural and social characteristics, became conditions of possibility when we integrated them from the beginning in the design process. The strong oral culture and the relatively widespread availability of phone communication, either mobile or fixed, led us into building a system of interactions based on audio across the digital divide using radio, phones and web technologies.</p>

					<p>In this way, the central vision of the project formed around the idea of a ‘living documentary’.  The idea was to enable the creation of a collective story by providing the conditions for the story to emerge, rather than simply “recording” it through a more traditional documentary approach.</p>

					<p>The testimonies shared will be simultaneously available on the phone line and through the web in audio form, allowing the participants to contribute to the narrative from both ends of technology. The project will thus create new methodologies by which people with little or no technical access can be equal participants in the process of making and accessing a documentary.</p>

					<p>To build the web interface we are taking inspiration from the Quipu. The Quipu is an ancient device made of threads and knots that the Inca's used to record collective oral stories. Its literal meaning in fact is Talking Knots. So in our interface, every testimony shared through the phone line becomes a thread, and every response from the web audience is added to the Quipu as a knot.</p>

					<strong>What are you looking to achieve by building out your story in this way?</strong>


					<p>Crucial to our idea of a living documentary, and the methodology we have developed to produce it,  is to harness the democratizing power of the Internet for the benefit of people who remain excluded from the digital world. By developing a repository of stories and testimonies, a sort of ‘offline webpage’, which people can contribute to through both high-tech (on-line) and low-tech (off-line) means, we expect that the collected material can be simultaneously available at the phone line and through the web in audio form, allowing the participants to contribute to the narrative from both ends of technology. In the long term, we believe this project has the potential to transform the historical memory of this case, from elite-driven towards participatory processes.</p>

					<p>It is important for us to stress that the Quipu project is not giving these women ‘a voice’, far from that we are the ones who are joining processes that were already taking place. These women have a voice and in many cases they have been speaking about what happened to them for more than 15 years, the only problem is that until now they haven’t been heard by many.</p>

					<p>In this sense, the central aim of the project lies in encouraging a meaningful dialogue in two directions: among those affected, and between those affected and the world. This kind of participatory projects have  a long tradition, probably the most notable is the Fogo Island Process that gave birth to the Challenge for Change programme promoted by the NFB since the 1960’s.  This form of dialogue is possible because it is driven by the people affected by the sterilization policy as they find the strength to continue their fight for justice. By widening the conversation, audiences will engage with the intimate human experience that is being shared. We have already witnessed, in our prototype project, the way in which women were energised by the process of giving their testimony through a phone, listening to it, and knowing that it was being shared publicly. </p>

					<p>Of course, many challenges lie ahead. Most crucially, to explore the digital medium in the crossover with offline media (radios and mobiles) to achieve a narrative form that expands the stories and trigger new possibilities of what an interactive documentary can be. </p>


					<strong>How are you planning to engage with the audience? </strong>

					<p>The contributors of these stories are also the first audience of the project. Most people affected by the sterilisation policy live in rural areas and don’t have access to Internet or new media technologies. So in order to make them active parts of the interactive experience we need to build a system that can bridge the digital divide through a combination of analog and digital media, local and global media. Our first prototype has allowed us to connect contributors in rural areas of Peru to participate by calling a free local phone number where they can share their stories and also listen to the stories shared by others. At first we were concerned if the technical quality of the shared testimonies would be enough and we didn’t even know if the women from the communities would feel comfortable sharing their stories through the phones. What we have found is that this simple mechanism is able to trigger testimonies with great expressive quality: the emotion and narrative rhythm of spoken word relates more to a very intimate documentary interview than what would be expected from a phone call. It is the st</p>rength of a an ancient oral culture, that has known how to convey knowledge through spoken words across ages that becomes alive once again.

					<p>There will be a parallel experience for the web audience, who will engage with these voices and find they can actually be part of this dialogue, either by simply acknowledging they have listened or by adding their own personal response. Quipu in fact means Talking Knots, so the main metaphor for the interface design is based on threads, knots and branches growing collectively a threaded structure in which audio testimonies and responses from the web audience are weaved together. The responses will not only be included in the web interface, they are carried back to the contributors in Peru through the same phone line and the dialogue goes on. In this sense, we expect the living documentary experience to grow and evolve to make the story happen in directions we can’t foresee at the moment.</p>
					<p>Both experiences connected, responding to each other, is what we expect will make the Quipu Project a distinct interactive experience.</p>


				
