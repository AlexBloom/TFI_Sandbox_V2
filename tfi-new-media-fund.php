					<strong>How do you envision to bring story, design and technology together? </strong>

					<p>Do not track explores how the personalization of the Internet affects our life as users, consumers and citizens. What better than a personalized program to answer to that question?</p>
				
					<p>We will mix both experience and investigation simultaneously in order to shed light on this more and more impenetrable universe.</p>
				
					<p>Do not track, will investigate tracking using the trackers' own technology. We will divide our audience in categories thanks to marketing tools and methods. We believe there is much potential in a “one-to-one” personalized experience in a program addressed to everyone. Its tone and form will be resolutely modern, dynamic and accessible to a mass audience. Our unique creative approach is to turn our audience into the subject of the documentary.</p>
				
					<p>The form will always fit with the content. We want to tell our story the same way as web giants do: using mobile phone, social networks or media websites.</p>
				
					<p>Likewise, our intention regarding design is to make the invisible visible. We want Internet users to shine a light on the opacity of Big data economy. Throughout the program, we will use colored dots as a metaphor of online trackers and data. We will show on screen what we know about each Internet user. The content as well as the interface will be personalized in order to create a unique and personal dialogue between the Internet user and the program.</p>
				
					<p>The global idea is to let people experience as much as possible how their datas are collected or used more than explain it. And make the most of every available tools that Internet offers to interact with our audience in order to adapt the program all along the conversation to what they give us.</p>
				
					<strong>What are you looking to achieve by building out your story this way?</strong>
				 
					<p>“Do not track” will show to the audience where their data goes and how it is used. We don’t tell if tracking is good or bad. But we shed light on this impenetrable universe. The goal is to understand how tracking and personalization distort our perception of reality.  We want to show the “bubble” that shapes our digital lives.</p>
				
					Do not track is divided in 2 Phases. The first one is an interactive film that individually addresses each Internet user to reveal “their” t<p>rackers. It promotes “Do not track” and creates the community.</p>
				
					<p>The second phase is a multi-media discussion (email, video, graphics, conferences) between the program and the users. It will begin with tracking in everyday life of Internet users, then explore the monetization of traffic and the economy of data brokers, and finally look at what shape online tracking could take in 30 years. The conversation builds a broader view of online tracking from the personal level up. This gradual approach from micro to macro allows us to reveal the limits of the bubble that surrounds our digital lives.</p>
				
					<p>The webdocumentary “Tracked” will end the online conversation.  It will follow six archetypal characters while they take part in the conversation: an investigative journalist, a data broker, a hyper-connected teenager, a job-hunting youth, etc. “Tracked” will makes use of the episodes of the conversation to dig into the issues faced by the characters and explore the intimate consequences of online tracking and the personalization of the Internet.</p>
				
					<p>We aim at focusing on different aspects of tracking, to allow users to interact with the program as they want, being “simple spectators” or “active contributors”. We want people to forge their own opinions by showing them what happens to their personal information and where their vision of the world could be altered. Asking them questions, helping them realize that the information they give “innocently” can be used in different manners through experience, that’s what we want to achieve more than giving them a unique point of view on a very complex and evolving issue.</p>
				
					<strong>How are you planning to engage with the audience? </strong>
					<p>Do not track is intrinsically an international project designed to reach a wide audience. Although the concepts of privacy and anonymity differ depending on countries and their cultures, this subject concerns everyone.</p>
					<p>The format of the interactive program and the conversation is genuinely engaging. We hope that the way we plan on doing it, in a very simple and intuitive manner will address a wide audience.</p>
					<p>Moreover, the distribution is at the very heart of the program. As we are used to at Upian, in “Do not track”, we work on conception, distribution and communication in parallel. In order to reach an international audience, we will work hand in hand with various medias around the world: we have started discussions with the New York Times in the United States, the BR in Germany or YLE in Finland. We are also looking for influential media partners in France, Germany and Canada. Our partners will be involved in editorial and artistic choices. They will offer to their audience a specific outlook reflecting their own culture. </p>
					<p>Our objective is to reach the audience using embed programs and viral diffusion instead of waiting for Internet users to come to our website. In this program, we are also thinking about the viral tools that can be used to bring some communities to our program. We are working on funny modules addressing different audiences with viral communications. Interactive or video short formats will explore some of the issues we treat and will be broadcast as the conversation advances, to make people aware of the subject. From false e-commerce sites selling you profiles to buy online cheaper, to video parody imagining conversations between human trackers, the idea is to laugh and share.</p>
				
			
