					<section id="GoingGranular"class="content anchor">
						<img src="img/content/build/projectmanagement/distribution-marisa.jpg" alt="Distribution Marisa">
						<h2>Going Granular: On Distribution &amp; Digital Inclusion</h2>
						<p>
						By Marisa Jahn, Artist and Writer, Executive Director of REV- <br>
						<a href="https://twitter.com/marisa_jahn>"target=_blank>@marisa_jahn</a><br>
						More from Marisa Jahn - <a href="tfi-new-media-fund-2012">Discover: New Media Fund 2012</a>
						</p>
						<p>
						Anjum Asharia, Program Director of REV-<br>
						<a href="https://twitter.com/yzplz"target=_blank>@yzplz</a> 
						</p>
						 
						<p><strong>Virus:</strong> Dude, this is totally gonna go viral! YouTube is gonna explode! 
						 </p>
						<p><strong>Vector:</strong> Sigh. You know, not every project’s success is determined by their YouTube views. 
						 </p>
						<p><strong>Virus:</strong> No?
						 </p>
						<p><strong>Vector:</strong> And, when you’re talking about going viral, you’re completely missing the <a href="http://www.pewinternet.org/fact-sheets/mobile-technology-fact-sheet/"target=_blank>10 million people in the U.S. without smart phones</a> or broadband access. Many of whom are likely to be immigrants, youth, low-wage or excluded workers –
						 </p>
						<p><strong>Virus:</strong> Excluded workers? Dude, please explain
						 </p>
						<p><strong>Vector:</strong> Well, when New Deal labor laws passed in the 1930s, they granted most workers basic protections like minimum wage and overtime pay, days of rest, coverage under OSHA laws, and protection against discrimination in the workplace. Only Southern lawmakers, seeking to control African-Americans, intentionally excluded domestic workers, shoe shiners, restaurant workers, farmworkers, etc. That’s what the <a href="http://www.unitedworkerscongress.org/"target=_blank>“excluded” worker group</a> refers to.  
						 </p>
						<p><strong>Virus:</strong> Oh. So how do you reach these folks if they don’t have data plans?
						 </p>
						<p><strong>Vector:</strong> Well there are lots of examples of apps for basic cell phone users in the Global South. Here in the States, less so. 
						 </p>
						<p><strong>Virus:</strong> What about yours? I heard you saying yours is a domestic worker app for any kind of phone.
						 </p>
						<p><strong>Vector:</strong> Well, in 2012, REV- produced a <a href="http://www.studiorev.org/projects/newday.htm"target=_blank>“public art nanny hotline”</a> — think “Click and Clack on NPR’s Car Talk but for nannies — seeking to inform the 200,000 nannies, housekeepers, and caregivers in the State of New York about their newfound rights under the 2010 Domestic Workers Bill of Rights. See, In New York, domestic workers are 95% immigrant women with low print literacy levels — so newspapers aren’t their primary source of information — who often work long hours and in isolation. We also know that they aren’t regularly listening to the radio. But one thing that most domestic workers have is at least a basic cheapie cell phone. So our challenge was to think of the cell phone not as a receiver but also as a broadcasting device that can deliver information to workers at any hour. Workers can call in using any kind of phone at any hour to hear about topics like overtime wage, paying your taxes, trafficking, and more. 
						 </p>
						<p><strong>Virus:</strong> So what’s your analytics like? 
						 </p>
						<p><strong>Vector:</strong> Well about 300-1200 people have called in per month. And we have seen a real uptick in calls when groups are running on the ground campaigns, or during peak nanny hiring seasons, for example. That means workers are not only using the tool themselves, but actively sharing it with their peers. 
						 </p>
						<p><strong>Virus:</strong> 300 to 1200 calls a month? Totally lame, bro.
						 </p>
						<p><strong>Vector:</strong> Not so!  While our numbers may seem like peanuts to you, we see a real impact. Think about it: many domestic workers — especially live-in nannies work in isolation. So you need new ways to reach them and meet them where they’re at, cuz they’re not going to go online on their employers’ computers and browse websites about their rights. 
						 </p>
						<p><strong>Virus:</strong> So then how do you reach them? 
						 </p>
						<p><strong>Vector:</strong> Well we have been providing media training to domestic worker groups in New York City for the past eight years. We craft the tools we develop closely with their members, because if they’re not involved it’s impossible to develop a tool that’s useful for them. For example, we know that nannies will reach out to each other in the park, the public library, or the street — but they don’t want a big ole flyer that might get accidentally left in the baby stroller. So we developed a business-card sized flyer that workers can discretely tuck into their wallets. 
						 </p>
						<p><strong>Virus:</strong> Cool so they can tell each other about the hotline. 
						 </p>
						<p><strong>Vector:</strong> Right. And actually one of the challenges we learned from early on was that we needed to work with domestic workers to create realistic scripts mirroring real-life situation that help them describe how this new tool works. We develop these scripts with members so that they can reach their peers. 
						 </p>
						<p><strong>Virus:</strong> Whoa. And are you also reaching out to other audiences?
						 </p>
						<p><strong>Vector:</strong> Well, we use traditional forms of social media to reach employers and inform them about their responsibilities and tips on what folks call “fair care” labor. And, right now, drawing from the <a href="https://civic.mit.edu/blog/beckyh/case-study-ndns-voip-drupal"target=_blank>successes and challenges</a> of the New York nanny hotline, we are working with <a href="http://www.domesticworkers.org/"target=_blank>The National Domestic Workers Alliance</a> to produce a souped up <a href="http://vimeo.com/84907598"target=_blank>national version</a>. 
						 </p>
						<p><strong>Virus:</strong> Souped up? How so?
						 </p>
						<p><strong>Vector:</strong> Well, you can join a live conference call, which is cool because domestic workers typically work long hours each week and often don’t have time to just go to a meeting. But this way they can join a conversation at a convenient time for them. Also, we’re also building in an interactive SMS component where workers can survey each with questions like, “Do you receive time and a half overtime pay? Respond ‘1’ for YES or ‘2’ for NO.” Or you can give a worker a prompt and ask them to call in to record their story. These interactive components are really important because historically, <a href="http://prospect.org/article/want-predict-future-surveillance-ask-poor-communities"target=_blank>marginalized people have endured high levels of direct forms of surveillance</a>. So involving domestic workers themselves in gathering data and stories about their peers reverses roles of power. It’s a different model of <a href="http://newamerica.net/publications/articles/2012/digital_inclusion_and_data_profiling_67367"target=_blank>digital inclusion</a>. 
						 </p>
						<p><strong>Virus:</strong> Digital inclusion?
						 </p>
						<p><strong>Vector:</strong> Yup — so check it out. So if <a href="http://newamerica.net/publications/articles/2012/digital_inclusion_and_data_profiling_67367"target=_blank>digital exclusion refers to the exclusion of chronically underserved communities like the poor, communities of color, indigenous groups, and migrants</a>, then digital inclusion means involving these underserved communities in practices that empower these folks in the collection, analysis, and transformation of data they collect about themselves. 
						 </p>
						<p><strong>Virus:</strong> And what’s being transformed?
						 </p>
						<p><strong>Vector:</strong> Oh — like the movement and organization of domestic workers or the laws themselves. Cuz one of our long term goals is to inform policy makers about the challenges domestic workers face and structurally improve the welfare of workers and by extension the rest of the population who rely on their labor to keep the wheels turning. 
						 </p>
						<p><strong>Virus:</strong> Gotcha. So not viral. Granular.
						 </p>
						<p><strong>Vector:</strong> That’s a good way of putting it. Going granular. 
						</p>

						
