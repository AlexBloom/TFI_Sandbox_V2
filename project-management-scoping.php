						<h2>Scoping</h2>
						<p><strong>By Mike Knowlton</strong>, Partner at Murmurco</p>

						<p>When starting to conceptualize an immersive media project, the scope is the first thing you need to establish. Subsequent tasks like creating a development timeline and budget are solely dependent upon the scope of the project.</p> 
						 
						<p>Scoping a project is used to establish the level of effort it will take to create and build it. Scoping starts by clearly identifying what exactly you are envisioning the project to be. This can be done by simply putting words to paper. </p>
						 
						<p>Describe the project goals, what it does, how it functions, how and where users interact with it, and lastly, if it will interface with any other third-party tools. This document should not be technical in nature, it’s main purpose is to establish in plain-english how the envisioned project works. </p> 
						 
						<p>A very simple example follows: </p>
						<p>Project X is a collaborative storytelling platform for people who are HIV+. Our goal is to show that people living with HIV lead normal lives. It will be a web-based tool that collects and displays user-submitted stories. We would like users to be able to connect via their Facebook account to gain access to their photos. A Project X “story” is approximately 3 paragraphs of text paired with any number of images a user uploads. </p>
						 
						<p>We would like users to be able to create and edit their stories then share them on Facebook. We would like all users with modern web browsers, tablets and smart phones to be able to use Project X. We envision an experience that is cinematic and includes beautiful animations and transitions. A video will be included on the main page of the website and we would like standard About/FAQ/Legal pages included on the site. </p> 
						 
						<p>From the document you create your digital partner will then be able to establish a set of design and technical requirements. From these requirements they will be able to identity the level of effort needed to design and build the project.</p> 
						 
						<p>Sounds simple right? Well, there are some gotchas:</p>
						<ul>	
							<li>	Make sure you clearly communicate the primary target audience for the project. Sometimes you may have competing target audiences, but you always want to establish the primary audience. There can only be one. 
							</li>
							<li>	Be crystal clear where this project will be experienced. Is it a live installation, a web app, or a mobile app?
							</li>
							<li>	Make sure to think about and communicate minimum performance specs. With all the devices out there, you need to establish what you want to minimally support. 
							</li>
							<li>	Break your project into phases. Communicate what functionality is “must have” vs. “nice to have”. One of the biggest mistakes people make is initially defining a project that is too big and complicated. 
							</li>
							<li>	Always consider the MVP (Minimum Viable Product). What is the very least amount of functionality you need to launch. Cut the bells and whistles and focus on the core attributes of your project. 
							</li>
						</ul> 
						<p>Creating immersive media projects is a blend of storytelling, filmmaking, user experience design, and technology development. Because of this it helps to think of your project in software development terms like prototypes, MVP, and phases of rollout.</p> 
						
