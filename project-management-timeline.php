						<h2>Immersive Storytelling Timelines: Don’t Forget To Fail</h2>
						<p>By Jason Jaacks, Founder of Split Frame Media, Director of Silent River Film, Return to Elwha &amp; Border Dispatch<br>
							<a href="https://twitter.com/jasonjaacks"target=_blank>@jasonjaacks</a> 
						</p>
						<img src="img/content/build/projectmanagement/timeline-example2.jpg" alt="Return To Elwha River"/>
						<p>Telling immersive stories online allows documentary filmmakers to re-imagine how audiences interact with our work. It’s an equally energizing and daunting task that permeates the project timeline and workflow.  Over the course of building a prototype for Border Dispatches and the Return to Elwha interactive documentary, I’ve discovered that the linchpin in making full use of the Internet as a mechanism for immersive storytelling is allowing ample time to fail. At the beginning of a new project, I sketch a rough timeline that invariably falls into this pattern:</p>

						<ul>
							<li> Dream/Brainstorm  </li>
							<li> Report/Shoot      </li>
							<li> Build             </li>
							<li> Test              </li>
							<li> Repeat!           </li>
						</ul>

						<p>The first couple steps look familiar; it’s the process digital storytellers brought to the field from other mediums. But the time spent on the front end Brainstorming, and after production is done Building, and Testing is crucial. Keep in mind Brainstorming, Building and Testing take as long or longer than production.</p> 

						<p>Successful interactive documentary projects are often conceived as such and allow for time upfront to consider questions such as “how will the end user navigate this story” and “will the interactions change by device”? Much of that early creative work informs every process thereafter. For instance, during my trips to the Elwha River I intentionally shot a lot of atmospheric b-roll that I knew I could later use as looping clips or background design elements. The Timeline should reflect the importance of prototyping ideas in the beginning. Before I pick up a camera or write a line of code, I spend time experimenting about how these two forms will work together within the project.</p>

						<p>Digital storytelling in the Internet Age allows us to quickly test our assumptions about how we structure and design our stories. Through the web, we have a direct line to our audiences, which we can utilize to create better work. Analytics and user feedback provide an incredible toolset for us as storytellers, but it takes a significant amount of time to use well.  The most important parts of the Timeline are Build, Test, Repeat. Whether it’s a two-week short project or a two-year documentary transmedia extravaganza, creating a Timeline with Build, Test, Repeat in mind helps make the project a success. </p>

						<p>Showing people working prototypes or near-finished products – and building in time to process all that feedback – seemed like unnecessary risk to me as a filmmaker. It can be a painstaking and aggravating part of the process (hence building in extra time to briefly step away), but the feedback that I have received is incredibly helpful. When I’ve built a Timline that allows for me to user test, I have been able to fail fast, fail often and fail forward. Ultimately, that process makes the project much, much stronger.</p>

