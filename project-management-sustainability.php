						<h2>Technology vs. Experience</h2>						

						<p>Being a media design firm, more often than not, a large concern of our clients is the sustainability of the technology being used. And quite frankly, more often than not, we too are skeptical of technology, but if done right digital technologies can create long lasting relationships between people and their surroundings. In fact, at Local Projects, it is built into the core of our mission to produce projects that will endure. We think of ourselves as translators of technology, integrating new technology into both the fabric of physical environments and our lives. We experiment and look for ways in which new technologies can allow people to participate in the experience, creating something much more engaging and that is meant to last. With each of our projects we look for ways in which people can further their connections to the world at large and to each other, creating experiences that produce emotional connections. These emotional experiences are what resonate with people on a deeper level, and what they will remember, not the latest technology that was used. As we look back on some of our past projects, while the technology may no longer seem cutting edge, the experience itself still resonates with visitors. As technology continues to evolve and integrate into our lives, creating new ways for us to share, reflect, and identify, we will be able to provide more and more engaging experiences that people will remember. Using this visitor experience centered thinking has allowed us to create timeless pieces and a sustainable approach for future projects.</p>

