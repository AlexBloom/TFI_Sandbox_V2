						<h2>How to Approach Sustainability</h2>
	
						<p>Sustainability is a tricky challenge in a crowd-sourced, social-media curated, web-based documentary project and one we still haven’t solved with 18DaysInEgypt. We built an innovative collaborative web-platform to empower Egyptians to tell their own story, in their own voice, using their own media. We had decided from the start that this was not our story to tell, instead choosing to trust our community, build our community, and let them make 18DaysInEgypt what it is today. We had to be sensitive to the political climate we were operating in, which means if one of our contributors removed one of their videos from Youtube, we automatically respected that decision in our platform and the linked content was also removed. These decisions have created a project that is a dynamic, living platform, a community that continually needs outreach, the social media tools we rely on are continuously evolving, and the archive itself needs a permanent resting place. </p>
						<p>At 18DaysInEgypt we have a huge challenge ahead of us about what it means to be sustainable and how to approach it. The obvious solutions are regular software updates with new social media API changes, installing a community manager to engage the community, and finding a suitable partner or institute where our archive can find its final resting place. </p>
						<p>However, all of the above are not sustainable solutions as they are requiring more funding, a permanent team, and the right partner - especially, when it’s almost impossible to define when a crowd-sourced, new media documentary project is actually “finished”. One approach we are considering is our final phase of the project, which will be our curated, produced take on 18DaysInEgypt. An offline, interactive experience reflecting on the Egyptian revolution and the year since, building on the over thousand stories and thousands of media contributed by our community.</p>
						
