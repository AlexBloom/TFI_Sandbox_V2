				<h2>The Hackathon Mentality</h2>
				<p class="intro">(or how collaboration and prototyping can improve the interactive storytelling ecosystem)</p>  

				

				<p>At Racontr - a code-free interactive storytelling software used by many interactive content producers and media outlets - and Storycode Paris - a series of monthly transmedia conferences and workshops - we constantly find ourselves involved with other people’s projects, helping them conceive, build and publish their interactive experiences. Project after project, we’ve been trying to refine an agile interactive production process that I wish to share with you.</p>

				<p><strong>Interactive and transmedia storytelling is and has to remain a great experimentation field.</strong> One quote by Pierre Cattan sums up what, to me, should be the ambition of any interactive and transmedia producer: </p>
					<blockquote> Transmedia storytelling is the name we gave to R&amp;D in the field of content creation.</blockquote>
					 <p>Devices are not done evolving, media are not done colliding, authors are not done transforming the way they build stories and audiences are not done embracing this &ldquo;lean forward&rdquo; position they are offered evermore frequently. </p>

				<p>That being said, <strong>experimentation should not be an excuse to waste resources.</strong> Experimentation does not mean that no method or rationalization processes can be devised and applied. Rationalizing resources and workflows allows for leaner, quicker and more innovative production process, while building on continuous learning to improve quality, innovation and efficiency over time.</p>

				<p>Rationalization does not imply that any content has to be identical to the other. It means though that any organization - from a couple of freelancers collaborating to the biggest media or production company - can find ways and tools to preserve its most valuable resources and skills for tasks providing the highest added value.</p>


				<h4>Interactivity is an investment</h4>

				<p>Few can say that their interactive storytelling works are a tremendous revenue source that can sustain their whole organization. For most, interactive pieces remain a vision, an investment towards a more user-centric future for online content. <strong>Basically, we’re doing R&amp;D.</strong> Much as any company in any industry invests time and resources towards innovation that could create a new economic system, a new market, and new opportunities in the future. </p>

				<p>The question then is: how do you optimize these investments and how do you get the best return on them? Surely the answer has to be different depending on the organization considered. However media, independent creators, producers have in common certain practices that generate a waste of resources, such as:</p>

				<ul>
					<li>	the constant rewriting of a project’s specifications over weeks/months of imprecise conceptualization 
					</li>
					<li>	the lack of technical anticipation
					</li>
					<li>	the absence of an interactive project manager
					</li>
					<li>	the sometimes endless contemplation of an ideal but unrealistic, unfeasible or unfundable experience 
					</li>
					<li>	the absence of a prototype providing user feedback in the development phase 
					</li>
					<li>	the instability of the creative team
					</li>
				</ul>

				<p>All those issues can be linked to three organizational defaults:</p>

				<ul>	
					<li>	the absence of an extensive and systematic conception and/or prototyping process
					</li>
					<li>	the difficult or lack of collaboration between people with complementary skills
					</li>
					<li>	the absence of an iterative process for continuous learning and innovation
					</li>
				</ul>

				<p>What we do, developing an interactive storytelling software such as Racontr, or hosting regular hackathons, is to provide all storytellers with the methods and tools to create interactive content differently, more rationally and more efficiently. </p>


				<h4>Ideas are meant to be hacked</h4>

				<p>Interactive production is perceived by many as an experiment but too few really adopt an experimental stance. More often than not, producers settle on applying a process derived from television or cinema production. For us creative technologists, it often means that people come to us when funding has been secured, with unclear specifications, to ask us to &ldquo;do it. And if you have some good ideas on how to improve the whole thing, feel free… .&rdquo; <strong>This only has the allure of collaboration but it is simply a more dilettante form of subcontracting.</strong></p>

				<p>True collaboration should start way upstream the production process. It is definitely more demanding and disruptive of the way things have been done for decades but this new mindset will greatly reduce the risk of unforeseen problems during the production process. </p>

				<p>Basically, I find that the best time to do this &ldquo;story hacking&rdquo; is pretty soon after the concept emerged, before the author or producer develops it to much in a single direction and gets too emotionally attached to it. The point is, the more you work on your own, on your very first idea, the more difficult it will be to welcome new ideas or even a complete creative destruction process. </p>

				<p>Because if you build a team, they will be eager to provide new ideas, to strengthen your project with their unique and valuable point of view and skill set. <strong>If you are not open to that collaboration, you don’t need a team&mdash;you need a workforce.</strong></p> 

				<p>We’ve experimented a lot on how, when, where is it best to set up those story hackathons. Each time we rearranged our method, trying to refine it into a series of guidelines we feel confident in sharing as good practices that can serve all of you interactive storytellers, wherever you are and whatever your project. <br> 
					Here are a few of those:</p>

				<p><strong>Do not wait.</strong> As I said, timing is key. A mere concept will soon become someone's &ldquo;baby&rdquo; you won’t be able to change without a great expense of time, negotiation and emotions. Assemble a team quickly to workshop all the possible developments of that original idea before you get stuck in a one-way street. </p>

				<p><strong>Find the right team.</strong> This is one of the greatest challenges of interactive production so ideally you should not look for collaborators when they are urgently needed to produce something you want. This collaborative effort must be engaged as early as possible to build personal bonds, a common language and trust between each member of a creative ecosystem.</p>

				<p><strong>Adapt your mindset.</strong> Members of the team might have to adapt their habits to work in that somewhat disruptive way. Authors have to make peace with losing some creative control for the good of their story. Technologists and designers should see themselves and also be considered as authors. Producers could benefit from taking a step back from the creative process and focus on producing: protecting the creative team, giving it the necessary tools and resources, fostering a creative work environment, finding funding for the project.</p>

				<p><strong>Ask all the right questions. </strong>Now that you have an idea and a team, go into full hackathon mode and challenge everything. You thought it should be an interactive documentary, well now imagine all the forms it could take on other platforms. You figured your audience would be everyone on the Internet, take a step back and do some audience design, find existing communities you can reach or partner. Etc.</p>

				<p>During our Storycode Paris two-days workshops we usually do 3 brainstorming sessions where each project is asked a series of questions about format, audience, user experience, business model, partnerships, etc. At the beginning, the point is to come up with as many ideas possible, pin them on the wall and then vote collectively to determine the most important ones. Others brainstorming sessions are meant to narrow down those multiple possibilities to find the essence of the project: what content and interactions are absolutely necessary for the story and the user experiencing it.</p>

				<p><strong>Preserve that hackathon mentality.</strong> We feel that this process can be adapted to any organization and should not be limited to a weekend experience during special events. We put a lot of effort into making our method as agile and easily adaptable in-house, with your team or your regular collaborators. After all, aside from the post-it and caffeine-based two-days method, it is mainly a mindset, a reflex you can adopt to generate more innovative ideas and foster a most efficient team dynamic.</p>


				<h4>Agility is key</h4>

				<p><strong>Now that you’ve had all those brilliant ideas, and you’ve made the conscious effort to determine what is the core of your experience, you can go forward and make stuff. Too often, interactive projects try to seek funding and partnerships with a video trailer and carefully chosen words. This is important for sure, but how much energy is wasted explaining with words, pitches, meetings what makes the strength of the project: the user experience.</strong></p>

				<p>Too many projects go straight from a concept on paper to actual production of the final product. Which means that funders and partners will never actually see anything live until it’s way too late to change anything without costs. </p>

				<p>It also means no user feedback was collected and acted upon during production development. <strong>I’ve never heard of a software company releasing a product without user testing and multiple alpha and beta versions. Why would interactive experiences be any different?</strong></p>

				<p>This is why I think agile software development methods such as SCRUM or Lean Startup can inspire us. Hugely popular among tech companies, they provide some very interesting insight for interactive producers, especially on the value of prototyping and iteration.</p>

				<p>Prototyping might appear financially challenging for producers evolving in a field where funding is scarce. But start with low-tech prototypes and slowly build towards more functional ones. You might want to use Twine or Scrivener as interactive writing tools, you won’t spend much. Or paper if you want.<strong> During this phase, I feel that three experiences must be designed: the Prototype; the Minimum Viable Experience; the Most Valuable Experience.</strong></p>

				<p>The Most Valuable Experience is your ideal project if all goes well. The issue here is to be ambitious but keeping the perimeter of the project credible and feasible. </p>

				<p>The Minimum Viable Experience is your ideal project reduced to its acceptable minimum to tell your story and give users a satisfying experience in spite of the lack of resources that forced you to go to plan B. Imagining this minimal experience enables you to identify what is the core of your project, and also where you draw the line under which doing it becomes unacceptable.</p>

				<p>And finally, design the Prototype you will use to test your concept, gather user feedback and make the necessary changes to your project during the development phase. Sure this will cost more resources that just writing a detailed file with some mockups. But what is the real cost for a producer or a author of month of imprecise conception, of unanticipated technical issues, of not being able to convince backers and partners because they can’t really &ldquo;touch and feel&rdquo; the experience you’re trying to sell them…?</p>

				<p>Many tools enable you to build these prototypes for a more than reasonable cost. Obviously, you guessed it, I will recommend you use Racontr, like the participants of the Tribeca Hacks <Racontr> did, and many others since then. The cost is using it will be negligible (if not nothing) and you will have a live demo for your project, a self-explanatory user experience that will become your best argument to make it happen.</p>

				<h4>Failure is cool</h4>

				<p><strong>If interactive storytelling is R&amp;D, it implies that sometimes you will fail.</strong> Whether you fail to push a project further than a concept on paper, fail to make a satisfying prototype to validate your idea, fail to get enough funding for your ideal project, fail to reach your target audience, you can still find a way to make it a worthy investment, provided that you’ve set up a continuous learning process.</p>

				<p><strong>Innovation can only be an iterative process.</strong> You won’t learn every lesson from one project, whether it succeeded or failed. But if you make a point of documenting a feedback on your creative experience, on your production process, on the tools you used, you will be building a knowledge base on which you can rely for your next project. And if everyone in your organization does the same, or if you share it with a global community such as Storycode, you can make sure that failure becomes a pretty cool thing where everyone contributes to reduce the risk of reproducing the same mistakes. </p>

				<p>During every Storycode Paris events we host, we invite majors interactive producers and creatives to come and share the mistakes and failures and the way they overcome all of them to achieve their goal, how they impacted the project, for better or worse.</p>

				<p>If we keep sharing resources and practical lessons as a community, we can identify more and more of these best practices and <strong>refine this collaborative production process that I feel is absolutely necessary to make our interactive storytelling experimentation field into a full-fledged industry</strong> where we can all thrive telling the stories we love.</p>
					
