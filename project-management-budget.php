							<h2>McCarren Park, a Passion-Fueled Project</h2> 
							<p><strong>By Caitlin Burns,</strong> Transmedia Producer</p>

							<p>In 2008, Steele Filipek and Caitlin Burns set out to create McCarren Park, a live-action comedy distributed through a mobile application (read: bar crawl) of Williamsburg, Brooklyn. The film was built as an homage to the low-budget monster movies of Ed Wood and, of course, as a parody of 1993’s Jurassic Park, riffing on the popularity of the hipster dinosaur meme. More than that, it was built to show that a multiplatform property could be produced and made with free platforms. </p>

							<p>Everyone joined the project because they were interested in the experiment: following in the steps of guerilla filmmakers, they wanted to create a project that any kid with a camera phone could learn from. The producers bootstrapped the hard costs of production. Actors donated their time. The Director of Photography used his personal Canon 5D to shoot. Simple, affordable, fun.</p>

							<p>Out of a total budget of $3500.00:</p>
							<ul>
								<li>The vast majority of the costs went to deposits on the space and staging of the final scene, which took place at a party in Brooklyn that the audience that had been built through characters interacting online through tumblr and twitter were invited to attend. 
								</li>
								<li>The second largest costs went to “catering” for the crew and cast during shoots. Actors drink a lot, after all.
								</li>
								<li>As DNA replication was ruled out from the start due to ethics, the third largest cost went to the construction of costumes and dinosaurs.
								</li> 
							</ul>
							<p>Notice a lack of advertisement: much of the project’s promotion folded in to the narrative development of the project. Shoots became invited live events for fans. Scenes were presented in character at local events that ultimately made their way into the final cut of the film. Twitter became a place where rehearsals turned into online scene weekly as actors built their characters and relationships. These were all free to use platforms; no pay-per-use platforms were utilized in the distribution of the project. The Movable Feast Mobile Media App was a fantastic partner as the support platform for the walking tour release of the film. The platform is free to download and use, pushing interactivity even further.</p>


							<p>At the start of the project, crowdfunding wasn’t really available (Kickstarter launched in 2009). It would have been a way for the project to find a larger audience, and a slightly larger budget, possibly outside of the low-budget context. More money would have meant a tighter production timeline for shooting. Outdoor shooting dates were spread out over the course of the summer of 2011 and were entirely at the mercy of volunteers’ schedules, the final scenes had to wait until later that year.</p> 

							<p>Passion fueled the project rather than money, and though it took longer than a standard production, people eventually took notice. The mobile app caught the attention of the 2012 Tribeca Interactive Day and 2012 New York Film Festival: proof positive of the exercise’s hypothesis and that, regardless of budget, everyone loves watching hipsters get eaten. </p>

