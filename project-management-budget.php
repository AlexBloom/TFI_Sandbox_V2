						<h2>Three Types of Implementation Options</h2> 
						<p><strong>By Mike Knowlton,</strong> Partner at Murmurco</p>

						<p>Project budgets are based upon the scope of a project, the level of effort it takes to make it. Accordingly budgets for immersive media projects can span a wide range. This is largely based on the approach that is taken during implementation.</p> 
						
						<p>There are three main types of implementation options:</p>
						
						<ol>
							<li>	<strong>Use existing technology platforms to create your project.</strong> There are a bevy of <a href="http://www.murmurco.com/landscape.htm"target=_blank>interactive storytelling platforms</a> available to creators these days. One implementation approach is to use one of these technology platforms. You may find there are functionality limitations, but these non-coding solutions provide a low cost option for creating interactive stores. Budget ranges are largely based on the time it takes for you to learn the software and to build your project and small monthly fees to host the project on a third-party interactive storytelling platform. 
								<br> <strong>Monthly cost ranges: $50 to $500</strong>
						<p>The next two implementation approaches are better suited for projects that require custom development that can’t be achieved with an existing interactive storytelling platform. </p>
							</li>
							<li>	<strong>Hire digital freelancers or people you know to help out.</strong> This implementation approach is better geared towards a truly bootstrapped or smaller crowd-funded project. This relies on knowing the right resources and being able to get them excited about your project. It’s not that different from building a film crew with people you know and trust who are willing to work at discounted rates because of your relationship and their excitement for your project. 
								<br> <strong>Budget Range: $5,000 to $15,000</strong>
							</li>
							<li>	<strong>Hire experts with a proven track record of success.</strong> This approach is required for projects that are truly innovative and breaking new ground. Brand new technology is created here to achieve the project’s goals. All of the immersive projects you see at major festivals, or read about in major publications or websites are created this way. 
								<br> <strong>Budget Range: $75,000 to $300,000</strong> 
							</li>
						</ol>	
						<p>Obviously these budget ranges vary tremendously. But realize that the immersive media projects that likely inspire you the most were created by a large team of experienced and talented professionals. The New York Times spent months and had an entire team working on the creation of <a href="http://www.nytimes.com/projects/2012/snow-fall/"target=_blank>Snow Fall</a>. Make sure to set your expectations accordingly based on the funds you have raised and the type of project you are trying to create. </p>
						
						<p>A final important aspect to consider is to establish how long the project will be live and then plan for ongoing support. A digital application is a living, breathing thing. It is vital to not only plan for its birth, but also its duration. The longer it lives the more expensive it is to support.</p>
						
						<p>Digital applications break for a variety of reasons (for example a new OS is released or the third-party Search Engine tool goes down). They need ongoing attention from developers, whether those are in or out-of-house. You should plan and budget accordingly.</p>
			
